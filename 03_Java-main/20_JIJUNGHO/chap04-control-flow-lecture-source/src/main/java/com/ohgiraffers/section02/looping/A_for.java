package com.ohgiraffers.section02.looping;

public class A_for {
    public void testSimpleForStatement() {

        // 1 ~ 10까지 1씩 증가시키면서, i값을 출력하는 기본 반복문
        // 동안 (1부터; 10까지; 1씩증가;){
        // 1부터 10까지 반복해서 출력
        // }
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
